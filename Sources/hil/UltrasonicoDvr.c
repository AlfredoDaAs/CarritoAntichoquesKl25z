void vfnInitUltrasonico(void){
	vfnInitGpio(PORTC);
	U08 u08PinEntrada[] = {12};
	U08 u08PinSalida[] = {12};
	vfnSetGPIOPortPins(PORTC, u08PinEntrada, 1, INPUT);
	vfnSetGPIOPortPins(PORTC, u08PinSalida, 1, OUTPUT);
}
